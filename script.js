// Enhanced Background Gradient with smooth transitions
function updateBackgroundGradient() {
    const scrollPercent = window.scrollY / (document.documentElement.scrollHeight - window.innerHeight);
    const hue1 = 245 + (Math.sin(scrollPercent * Math.PI * 2) * 15);
    const hue2 = 260 + (Math.cos(scrollPercent * Math.PI * 2) * 20);
    const hue3 = 210 + (Math.sin(scrollPercent * Math.PI * 4) * 25);
    
    document.body.style.background = `linear-gradient(135deg, hsl(${hue1}, 45%, 15%) 0%, hsl(${hue2}, 50%, 20%) 50%, hsl(${hue3}, 55%, 25%) 100%)`;
    document.body.style.backgroundSize = '400% 400%';
}

window.addEventListener('scroll', updateBackgroundGradient);

